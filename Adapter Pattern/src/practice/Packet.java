package practice;

public class Packet {
	private double height;
	private double width;
	private double depth;

	public Packet(double height, double width, double depth) {
		this.height = height;
		this.width = width;
		this.depth = depth;
	}

	public double getHeight() {
		return height;
	}

	public double getWidth() {
		return width;
	}

	public double getDepth() {
		return depth;
	}

}
